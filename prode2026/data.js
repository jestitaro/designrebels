/**
 * Calcular puntos de una predicción vs resultado real.
 * pred y real son 'A' | 'X' | 'B'
 * Retorna: { puntos, tipo: 'acierto'|'nada' } o null si no hay resultado
 */
window.calcularPuntos = function(pred, real) {
  if (!real) return null;
  if (pred === real) return { puntos: PUNTOS.acierto, tipo: 'acierto' };
  return { puntos: 0, tipo: 'nada' };
};

